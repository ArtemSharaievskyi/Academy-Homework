// Забезпечуємо наявність глобального сховища
global.users = global.users || {};

// Зчитуємо всі аргументи командного рядка
const args = process.argv.slice(2);

// Перевіряємо, чи є серед них флаг '--reset'
if (args.includes('--reset')) {
  // Очищаємо глобальне сховище користувачів
  global.users = {};
  
  console.log('Глобальне сховище користувачів очищено.');
  process.exit(0);
}

// Якщо флагу немає — виводимо поточний стан
console.log('Поточне глобальне сховище користувачів:', global.users);
console.log('Щоб очистити сховище, запустіть з флагом --reset');
